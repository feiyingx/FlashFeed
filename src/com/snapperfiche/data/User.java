package com.snapperfiche.data;

public class User {
	private String email;
	private String alias;
	private String accounttype;
	
	public User(){}
	
	public String getEmail(){
		return email;
	}
	
	public String getAlias(){
		return alias;
	}
	
	public String getAccountType(){
		return accounttype;
	}
}
